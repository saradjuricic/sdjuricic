<script>
google.charts.load('current', {'packages':['corechart']});
google.charts.setOnLoadCallback(drawChart);

function drawChart() {
    // Get data from HTML data attribute
    var chartElement = document.getElementById('ordersChart');
    var rawData = JSON.parse(chartElement.getAttribute('data-chart-data'));
    
    // Build Google Charts data array
    var dataArray = [['Date', 'Orders']];
    
    // Add each data point
    rawData.forEach(function(item) {
        dataArray.push([item.date, parseInt(item.count)]);
    });
    
    var data = google.visualization.arrayToDataTable(dataArray);

    var options = {
        title: 'Daily Orders',
        curveType: 'function',
        legend: { position: 'bottom' }
    };

    var chart = new google.visualization.LineChart(chartElement);
    chart.draw(data, options);
}
</script>